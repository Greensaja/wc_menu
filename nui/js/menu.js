    _adjust(dir) {
        const items = this.state.payload?.items || [];
        const item = items[this.state.selectedIndex];
        if (!item) return;
        switch (item.type) {
            case 'slider':
            case 'quantity': {
                const step = item.step || 1;
                const min = item.min ?? 0, max = item.max ?? 100;
                const cur = item.value ?? min;
                const next = Math.min(max, Math.max(min, cur + dir * step));
                if (next !== cur) {
                    WCSound.play('nav');
                    item.value = next;
                    this.render();
                    wcPost('change', { index: this.state.selectedIndex + 1, value: next });
                }
                break;
            }
            case 'checkbox': {
                if (dir === 0) return;
                WCSound.play('select');
                item.value = !item.value;
                this.render();
                wcPost('change', { index: this.state.selectedIndex + 1, value: item.value });
                break;
            }
            case 'select': {
                const opts = item.options || [];
                if (!opts.length) return;
                const curIdx = Math.max(0, opts.findIndex(o => o.value === item.value));
                const nextIdx = (curIdx + dir + opts.length) % opts.length;
                WCSound.play('nav');
                item.value = opts[nextIdx].value;
                this.render();
                wcPost('change', { index: this.state.selectedIndex + 1, value: item.value });
                break;
            }
        }
    },

    activate() {
        // If a confirm dialog is open, trigger the current choice
        if (this.state.confirming) {
            if (this.state.confirmChoice === 0) this._confirmYes();
            else this._confirmNo();
            return;
        }

        const items = this.state.payload?.items || [];
        const item = items[this.state.selectedIndex];
        if (!item || item.disabled) {
            WCSound.play('error');
            if (item?.disabledReason && window.WCToast) {
                WCToast.show(item.disabledReason, 'lock', 1800);
            }
            return;
        }

        if (item.type === 'checkbox') {
            WCSound.play('select');
            item.value = !item.value;
            this.render();
            wcPost('change', { index: this.state.selectedIndex + 1, value: item.value });
            return;
        }

        if (item.confirm) {
            WCSound.play('nav');
            this.state.confirming = item;
            this.state.confirmChoice = 0;
            this._renderConfirm();
            return;
        }

        WCSound.play(item.price ? 'confirm' : 'select');
        wcPost('select', { index: this.state.selectedIndex + 1 });
    },

