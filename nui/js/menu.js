    activateSearch() {
        if (this.state.confirming) return;
        this.state.searchActive = true;
        this.state.searchQuery = '';
        this.state.searchSelectedIndex = 0;
        this.render();
    },

    tab() {
        if (this.state.confirming || this.state.searchActive) return;
        wcPost('tab', { index: this.state.selectedIndex + 1 });
    },

    deactivateSearch() {
        this.state.searchActive = false;
        this.state.searchQuery = '';
        this.state.searchSelectedIndex = 0;
        this.render();
    },

    _getFilteredItems() {
        const q = this.state.searchQuery.toLowerCase().trim();
        const items = this.state.payload?.items || [];
        const selectable = items.filter(i => i.type !== 'divider' && i.type !== 'label');
        if (!q) return selectable;
        return selectable.filter(i => (i.label || '').toLowerCase().includes(q));
    },

    _renderSearch() {
        const contentEl = document.getElementById('wc-content');
        if (!document.getElementById('wc-search-input')) {
            contentEl.innerHTML = `
                <div class="wc-search-bar">
                    <i class="fa-solid fa-magnifying-glass wc-search-icon"></i>
                    <input id="wc-search-input" class="wc-search-input"
                           type="text" value="" placeholder="Search..."
                           autocomplete="off" spellcheck="false">
                </div>
                <div class="wc-header-divider"></div>
                <div id="wc-search-results" class="wc-items"></div>`;
            const inp = document.getElementById('wc-search-input');
            inp.addEventListener('input', () => {
                this.state.searchQuery = inp.value;
                this.state.searchSelectedIndex = 0;
                this._updateSearchResults();
            });
            inp.addEventListener('keydown', (e) => {
                if (e.key === 'Escape' || (e.key === 'Backspace' && !inp.value)) {
                    e.preventDefault();
                    this.deactivateSearch();
                } else if (e.key === 'ArrowUp') {
                    e.preventDefault(); e.stopPropagation();
                    this._searchNav(-1);
                } else if (e.key === 'ArrowDown') {
                    e.preventDefault(); e.stopPropagation();
                    this._searchNav(1);
                } else if (e.key === 'Enter') {
                    e.preventDefault(); e.stopPropagation();
                    this._searchSelect();
                }
            });
            setTimeout(() => { const inp = document.getElementById('wc-search-input'); if (inp) inp.focus(); }, 10);
        }
        this._updateSearchResults();
    },

    _updateSearchResults() {
        const resultsEl = document.getElementById('wc-search-results');
        if (!resultsEl) return;
        const filtered = this._getFilteredItems();
        const selIdx = this.state.searchSelectedIndex;
        resultsEl.innerHTML = filtered.length
            ? filtered.map((item, i) => {
                const renderer = WCItem[item.type] || WCItem.button;
                return renderer(item, i === selIdx);
              }).join('')
            : '<div class="wc-search-empty">No results found</div>';
        this._bindSearchItemEvents(filtered);
    },

    _bindSearchItemEvents(filtered) {
        document.querySelectorAll('#wc-search-results .wc-item').forEach((el, i) => {
            el.addEventListener('click', () => {
                this.state.searchSelectedIndex = i;
                this._searchSelectAt(i, filtered);
            });
            el.addEventListener('mouseenter', () => {
                if (this.state.searchSelectedIndex !== i) {
                    this.state.searchSelectedIndex = i;
                    this._updateSearchResults();
                }
            });
        });
    },

    _searchNav(dir) {
        const filtered = this._getFilteredItems();
        if (!filtered.length) return;
        const n = filtered.length;
        this.state.searchSelectedIndex = (this.state.searchSelectedIndex + dir + n) % n;
        WCSound.play('nav');
        this._updateSearchResults();
    },

    _searchSelect() {
        const filtered = this._getFilteredItems();
        this._searchSelectAt(this.state.searchSelectedIndex, filtered);
    },

    _searchSelectAt(i, filtered) {
        const item = filtered[i];
        if (!item) return;
        const items = this.state.payload?.items || [];
        const realIndex = items.findIndex(it => it === item);
        if (realIndex < 0) return;
        this.state.selectedIndex = realIndex;
        this.deactivateSearch();
        this.activate();
    },
